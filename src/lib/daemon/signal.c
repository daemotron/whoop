#include <errno.h>
#include <stdlib.h>
#include <signal.h>

#include "_daemon.h"
#include "config.h"

extern int
_daemon_handle_signal(int sig_no, _daemon_sighandler_t signalhandler)
{
	struct sigaction action;

	action.sa_handler = signalhandler;
	sigemptyset(&action.sa_mask);
	action.sa_flags = 0;

	if (sigaction(sig_no, &action, NULL) < 0)
		return 0;

	return 1;
}

